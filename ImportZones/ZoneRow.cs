namespace Geotab.SDK.ImportZones
{
    /// <summary>
    /// A zone
    /// </summary>
    class ZoneRow
    {
        /// <summary>
        /// The latitude
        /// </summary>
        public float Latitude;

        /// <summary>
        /// The longitude
        /// </summary>
        public float Longitude;

        /// <summary>
        /// The name
        /// </summary>
        public string Name;

        /// <summary>
        /// The node name
        /// </summary>
        public string NodeName;

        /// <summary>
        /// The size
        /// </summary>
        public float Size;
    }
}